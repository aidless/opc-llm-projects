def cail_to_graphrag_doc(case: dict[str, Any]) -> dict[str, Any]:
    """CAIL 案件 → GraphRAG-CN JSONL 格式."""
    # 提取参与方
    participants = []
    if "judges" in case:
        for j in case["judges"]:
            participants.append({"name": j, "role": "法官"})
    # Gold entities
    gold_event = case.get("subcategory", case.get("category", "案件"))
    gold_entities = [{
        "event": f"{case.get('subcategory', '案件')}",
        "type": case.get("category", "法律"),
        "date": f"{case.get('year', 2020)}-01-01",
        "participants": participants,
    }]

    return {
        "id": case["case_id"],
        "category": case.get("category", "legal"),
        "text": case["text"],
        "query": f"{case['title']} 的判决结果",
        "answer": f"{case.get('court', '法院')} 判决",
        "gold_entities": gold_entities,
        "timestamp": f"{case.get('year', 2020)}-06-01",
        "split": "test" if case.get("year", 2020) >= 2021 else "train",
    }


def load_cail_cases() -> list[dict[str, Any]]:
    """加载所有 CAIL 内置真实案件."""
    return [cail_to_graphrag_doc(c) for c in CAIL_REAL_CASES]


def save_cail_jsonl(output_path: str | Path) -> int:
    """保存 CAIL 案件为 JSONL."""
    docs = load_cail_cases()
    with open(output_path, "w", encoding="utf-8") as f:
        for d in docs:
            f.write(json.dumps(d, ensure_ascii=False) + "\n")
    return len(docs)


def get_cail_stats() -> dict[str, Any]:
    """CAIL 数据集统计."""
    cases = CAIL_REAL_CASES
    by_category = {}
    by_subcategory = {}
    for c in cases:
        by_category[c["category"]] = by_category.get(c["category"], 0) + 1
        sub = c.get("subcategory", "unknown")
        by_subcategory[sub] = by_subcategory.get(sub, 0) + 1
    return {
        "n_total": len(cases),
        "by_category": by_category,
        "by_subcategory": by_subcategory,
        "year_range": [min(c["year"] for c in cases), max(c["year"] for c in cases)],
    }


